    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        int fila = jTableEmpresas.getSelectedRow();
        if (fila != -1) {
            this.accion = Controlador.MODIFICAR;
            this.activarComponents();
            controladorEmpresa.setSeleccionado(this.listaEmpresas.get(fila));

            Empresa oempresa;
            oempresa = this.listaEmpresas.get(jTableEmpresas.getSelectedRow());
            this.persona = oempresa.getDniRepresentante();
            txtRepresentante.setText(oempresa.getDniRepresentante().getApellidos()+" "+oempresa.getDniRepresentante().getNombres());
            txtRucEmpresa.setText(controladorEmpresa.getSeleccionado().getRuc());
            txtNombreEmpresa.setText(controladorEmpresa.getSeleccionado().getNombre());
            txtDireccion.setText(controladorEmpresa.getSeleccionado().getDireccion());          
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtRucEmpresa.getText().equals("") || txtNombreEmpresa.getText().equals("") || txtDireccion.getText().equals("") || txtRepresentante.getText().equals("")) {
            ClaseUtil.mensajeExito(this,4);
        } else {
            controladorEmpresa.getSeleccionado().setRuc(txtRucEmpresa.getText().trim().toUpperCase());
            controladorEmpresa.getSeleccionado().setNombre(txtNombreEmpresa.getText().trim().toUpperCase());
            controladorEmpresa.getSeleccionado().setDireccion(txtDireccion.getText().trim().toUpperCase());
            controladorEmpresa.getSeleccionado().setDniRepresentante(this.persona);            

            if (this.accion == Controlador.NUEVO) {
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorEmpresa.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }else if(this.accion == Controlador.MODIFICAR){
                if (ClaseUtil.dialogoConfirmar(this, this.accion)) {
                    controladorEmpresa.accion(this.accion);
                    ClaseUtil.mensajeExito(this, accion);
                }
            }
            this.presentarDatos();
            this.accion = 0;
            this.activarComponents();
            ClaseUtil.limpiarComponente(jPanelDatos);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

